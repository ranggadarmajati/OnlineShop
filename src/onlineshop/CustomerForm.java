    private void TransactionDetination() throws SQLException
    {
        String province = (String) jCmbProvince.getSelectedItem();            
        String sql_get_trans_id ="SELECT count(id) as transaction_id FROM transaction";
        pst=con.prepareStatement(sql_get_trans_id);
        rs = pst.executeQuery();
        if(rs.next()){
            sql ="INSERT INTO `transaction_destination` (`transaction_id`, `province`, `city`, `subdistrict`, `zip_postal`, `address`) VALUES (?,?,?,?,?,?)";
            pst=con.prepareStatement(sql);
            pst.setString(1, rs.getString("transaction_id"));
            pst.setString(2, (String) jCmbProvince.getSelectedItem());
            pst.setString(3, txtCity.getText());
            pst.setString(4, txtSubdistrict.getText());
            pst.setString(5, txtZipostal.getText());
            pst.setString(6, txtAddress.getText());
            pst.execute();
            TransactionDetail(rs.getString("transaction_id"));
        }
                    
    }
    
