    private void TransactionDetail(String tid) throws SQLException
    {
        String get_all_tmp ="SELECT * FROM tmp_cart where user_id=?";
                    pst=con.prepareStatement(get_all_tmp);
                    pst.setString(1, txtId.getText());
                    ResultSet rs_tmp = pst.executeQuery();
                    String transac_id = tid;
                        while(rs_tmp.next()){
                            String item_id = rs_tmp.getString("id");
                            String item_name = rs_tmp.getString("name");
                            String qty = rs_tmp.getString("qty");
                            String item_price = rs_tmp.getString("price");
            int total_price = Integer.parseInt(rs_tmp.getString("qty"))*Integer.parseInt(rs_tmp.getString("price"));
                            String sql_transaction_detail = "INSERT INTO `transaction_detail` (`transaction_id`, `item_id`, `item_name`, `qty`, `item_price`, `total_price`) VALUES (?, ?, ?, ?, ?, ?)";
                            pst=con.prepareStatement(sql_transaction_detail);
                            pst.setString(1, transac_id);
                            pst.setString(2, item_id);
                            pst.setString(3, item_name);
                            pst.setString(4, qty);
                            pst.setString(5, item_price);
                            pst.setString(6, Integer.toString(total_price));
                            pst.execute();
                        }
    }
    
