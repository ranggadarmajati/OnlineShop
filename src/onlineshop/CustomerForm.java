    private void btnAddCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCartActionPerformed
        try {
                sql = "INSERT INTO tmp_cart (id, name, price, qty, weight, user_id) VALUES (?, ?, ?, ?, ?, ?)";
                pst=con.prepareStatement(sql);
                pst.setString(1, txt_id_brg.getText());
                pst.setString(2, (String) ComboBoxBrg.getSelectedItem());
                pst.setString(3, txtHarga.getText());
                pst.setString(4, txtQty.getText());
                pst.setString(5, txtWeight.getText());
                pst.setString(6, txtId.getText());
                boolean cek;
                cek = pst.execute();
                if(cek){
                    JOptionPane.showMessageDialog(null, "gagal menambah barang ke cart");
                }else{
                    
                    cart_table();
                    sub_total();
                    JOptionPane.showMessageDialog(null, "berhasil menambah barang ke cart");
                }
                
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnAddCartActionPerformed

