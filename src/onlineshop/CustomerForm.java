    private void sub_total()
    {
        try{
            sql = "SELECT sum(price * qty) as subtotal FROM tmp_cart where user_id=?";
            pst = con.prepareStatement(sql);
            pst.setString(1, txtId.getText());
            rs = pst.executeQuery();
            if(rs.next()){
                String subTotal;
                subTotal = rs.getString("subtotal");
                if(subTotal == null){
                    subTotal = "0";
                }
                txtSubTotal.setText(subTotal);
            }
        } catch (SQLException e){
               JOptionPane.showMessageDialog(this, e.getMessage()); 
        }
    }        
    
    private void cart_table()
    {
        DefaultTableModel tb= new DefaultTableModel();
        tb.addColumn("ID BARANG");
        tb.addColumn("NAMA BARANG");
        tb.addColumn("HARGA");
        tb.addColumn("QTY");
        tb.addColumn("TOTAL");
        jTableTmp.setModel(tb);
        try { 
            sql = "SELECT *, (tmp_cart.price * tmp_cart.qty) as total FROM tmp_cart where user_id=?";
            pst=con.prepareStatement(sql);
            pst.setString(1, txtId.getText());
            rs = pst.executeQuery();
            
            while(rs.next()){
                tb.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getString("name"),
                    rs.getString("price"),
                    rs.getString("qty"),
                    rs.getString("total"),
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
