    private void sub_total(String trans_id)
    {
        try { 
            sql = "SELECT SUM(total_price) as subtotal FROM `transaction_detail`WHERE transaction_id=?";
            pst =con.prepareStatement(sql);
            pst.setString(1, trans_id);
            rs = pst.executeQuery();
            
            if(rs.next()){
               txtSUBTOTAL.setText(rs.getString("subtotal"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void detail_table(String trans_id)
    {
        DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("NAMA BARANG");
        tbl.addColumn("QTY");
        tbl.addColumn("HARGA");
        tbl.addColumn("TOTAL");
        jTableDetail1.setModel(tbl);
        try { 
            sql = "SELECT * FROM `transaction_detail`WHERE transaction_id=?";
            pst =con.prepareStatement(sql);
            pst.setString(1, trans_id);
            rs = pst.executeQuery();
            
            while(rs.next()){
                tbl.addRow(new Object[]{
                    rs.getString("item_name"),
                    rs.getString("qty"),
                    rs.getString("item_price"),
                    rs.getString("total_price"),
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void trans_destination(String trans_id)
    {
        try { 
            sql = "SELECT * FROM `transaction_destination`WHERE transaction_id=?";
            pst =con.prepareStatement(sql);
            pst.setString(1, trans_id);
            rs = pst.executeQuery();
            
            if(rs.next()){
               txtProvince.setText(rs.getString("province"));
               txtCity.setText(rs.getString("city"));
               txtSubdistrict.setText(rs.getString("subdistrict"));
               txtZipostal.setText(rs.getString("zip_postal"));
               txtAddress.setText(rs.getString("address"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void get_payment(String trans_id)
    {
        try { 
            sql = "SELECT * FROM `transaction`WHERE id=?";
            pst =con.prepareStatement(sql);
            pst.setString(1, trans_id);
            rs = pst.executeQuery();
            
            if(rs.next()){
               lblNoRek.setText(rs.getString("no_rek"));
               lblBank.setText(rs.getString("bank"));
               lblAn.setText(rs.getString("atas_nama"));
               lblTfr.setText(rs.getString("nominal_trans"));
               BufferedImage img = null;
                           try {
                                 img = ImageIO.read(getClass().getResource(rs.getString("proof_of_payment")));
                           } catch (IOException e) {
                                        e.printStackTrace();
                           }
                           Image dimg = img.getScaledInstance(lblImgTfr.getWidth(), lblImgTfr.getHeight(), Image.SCALE_SMOOTH);
                        ImageIcon image = new ImageIcon(dimg);
                        lblImgTfr.setIcon(image);
                        String StatByr = rs.getString("is_transfer");
                        String StatCrm = rs.getString("is_confirm");
                        LoginBtnKonfir(StatByr, StatCrm);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
