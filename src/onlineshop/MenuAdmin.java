    private void table_konfirm()
    {
        DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("Trans ID");
        tbl.addColumn("Customer");
        tbl.addColumn("Total Qty");
        tbl.addColumn("Total Harga");
        tbl.addColumn("Pembayaran");
        tbl.addColumn("Konfirmasi");
        jTableKonfirm.setModel(tbl);
        try{
            sql = "SELECT t.id, u.nama, t.total_qty, t.price_total, (CASE WHEN t.is_transfer = 0 THEN 'Belum di Bayar' ELSE 'Sudah di Bayar' END) as is_transfer, (CASE WHEN t.is_confirm = 0 THEN 'Belum' ELSE 'Sudah' END) AS is_confirm FROM `transaction` as t JOIN user u on u.id = t.user_id";
            pst =con.prepareStatement(sql);
            rs = pst.executeQuery();
            
            while(rs.next()){
                tbl.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getString("nama"),
                    rs.getString("total_qty"),
                    rs.getString("price_total"),
                    rs.getString("is_transfer"),
                    rs.getString("is_confirm"),
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

